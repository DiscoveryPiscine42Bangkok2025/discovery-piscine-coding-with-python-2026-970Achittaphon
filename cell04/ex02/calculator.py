#!/usr/bin/env python3

first_n = int(input("Give me the first number: "))
second_n = int(input("Give me the second number: "))
print("Thank you!")

print(f"{first_n} + {second_n} = {first_n + second_n}")
print(f"{first_n} - {second_n} = {first_n - second_n}")
print(f"{first_n} / {second_n} = {int(first_n / second_n)}")
print(f"{first_n} * {second_n} = {first_n * second_n}")